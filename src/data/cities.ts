export interface CityData {
  region: string;
  name: string;
  job: number;
  entrepreneur: number;
  lieFlat: number;
  safety: number;
  infra: number;
  family: number;
  female: number;
  smoking: number;
  moral: number;
  green: number;
}

export const parseCities = (): CityData[] => {
  const lines = rawCsv.trim().split('\n').slice(1); // skip header
  let currentRegion = '';
  return lines.map(line => {
    const parts = line.split(',');
    if (parts[0]) {
      currentRegion = parts[0];
    }
    return {
      region: currentRegion,
      name: parts[1],
      job: parseInt(parts[2], 10),
      entrepreneur: parseInt(parts[3], 10),
      lieFlat: parseInt(parts[4], 10),
      safety: parseInt(parts[5], 10),
      infra: parseInt(parts[6], 10),
      family: parseInt(parts[7], 10),
      female: parseInt(parts[8], 10),
      smoking: parseInt(parts[9], 10),
      moral: parseInt(parts[10], 10),
      green: parseInt(parts[11], 10),
    };
  });
};

